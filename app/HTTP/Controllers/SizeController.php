     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            "name" => "required|alpha_num|uppercase"
        ]);

        $size = new Size();
        $size->fill($request->post());
        $size->save();

        return redirect("/admin/sizes")->with("success", "The size is added successfully");
    }

